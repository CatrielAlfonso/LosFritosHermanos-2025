create table public.pedidos (
  id bigint generated by default as identity not null,
  comidas jsonb[] not null,
  bebidas jsonb[] not null,
  postres jsonb[] null,
  precio real null,
  tiempo_estimado smallint null,
  confirmado boolean null default false,
  mesa text null,
  estado text null default 'Esperando confirmacion'::text,
  estado_comida text null,
  estado_bebida text null,
  estado_postre text null,
  recepcion boolean null default false,
  cuenta text null,
  pagado real null,
  cliente_id uuid null,
  fecha_pedido timestamp with time zone null,
  motivo_rechazo text null,
  observaciones_generales text null,
  solicita_cuenta boolean null default false,
  cuenta_habilitada boolean null default false,
  constraint pedidos_pkey primary key (id),
  constraint pedidos_cliente_id_fkey foreign KEY (cliente_id) references clientes (uid)
) TABLESPACE pg_default;