create table public.mesas (
  id bigint generated by default as identity not null,
  numero smallint not null,
  tipo text null,
  imagen text null,
  qr text null,
  comensales smallint null,
  ocupada boolean null default false,
  "clienteAsignadoId" bigint null,
  pedido_id bigint null,
  constraint mesas_pkey primary key (id),
  constraint mesas_clienteAsignadoId_fkey foreign KEY ("clienteAsignadoId") references clientes (id),
  constraint mesas_pedido_id_fkey foreign KEY (pedido_id) references pedidos (id)
) TABLESPACE pg_default;