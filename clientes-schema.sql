create table public.clientes (
  id bigint generated by default as identity not null,
  nombre text not null,
  apellido text null,
  "imagenPerfil" text null,
  dni text null,
  correo text null,
  anonimo boolean null default false,
  fcm_token text null,
  sentado boolean null default false,
  finalizado boolean null default false,
  encuesta boolean null default false,
  validado boolean null,
  aceptado boolean null,
  uid uuid null,
  descuento_ganado boolean null,
  porcentaje_desc bigint null,
  constraint clientes_pkey primary key (id),
  constraint clientes_uid_key unique (uid)
) TABLESPACE pg_default;